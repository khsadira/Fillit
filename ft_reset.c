#include "fillit.h"

void	ft_reset_tuint16(t_uint16 *map)
{
	int		i;

	i = 0;
	while (i < 16)
		map[i++] = 0;
}

void	ft_reset_int(int *pos)
{
	int		i;

	i = 0;
	while (i < 26)
		pos[i++] = 0;
}
